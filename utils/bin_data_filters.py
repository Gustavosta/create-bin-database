def bank_filter(bank: str):
    """
    Filters the bank name.

    Args:
        bank (str): Bank name.

    Returns:
        str: Filtered bank name.
    """

    if bank.lower().strip() == '':
        return ''
    return bank if not bank.isupper() else bank.title()


def type_filter(card_type: str):
    """
    Filters the card type.

    Args:
        card_type (str): Card type.

    Returns:
        str: Filtered card type.
    """

    if card_type.lower().strip() == '':
        return ''
    return card_type.title()


def scheme_filter(scheme: str):
    """
    Filters the scheme.

    Args:
        scheme (str): Card Scheme.

    Returns:
        str: Filtered scheme.
    """

    if scheme.lower().strip() == '':
        return ''
    elif scheme.lower() == 'american express':
        return 'AMEX'
    return scheme.upper()


def level_filter(level: str, scheme: str):
    """
    Filters the level.

    Args:
        level (str): Card Level.
        scheme (str): Card scheme.

    Returns:
        str: Filtered level.
    """

    if level.lower().strip() == '' or '/' in level:
        return ''
    elif scheme.lower() == 'amex' or scheme.lower() == 'american express':
        return 'AMEX'
    return level.upper()


